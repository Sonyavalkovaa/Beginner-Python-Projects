yearofbirth = int(input("Year born: "))
if yearofbirth <= 1946: 
  print("You are part of the Traditionalists generation.")
elif yearofbirth <= 1964: 
  print("You are part of the Baby Boomers generation.")
elif yearofbirth <= 1981: 
  print("You are part of Generation X.")
elif yearofbirth <= 1995: 
  print("You are part of the Millenials generation.")
elif yearofbirth <= 2015:
  print("You are part of Generation Z.")